# This is input
number1 = float(input ("what is your first number?"))
number2 = float(input ("What is your second number?")) 

# This is processing
add = number1 + number2 
sub = number1 - number2 
mult = number1 * number2 
div = number1 / number2 
mod = number1 % number2 


# This is output... displaying the result to the user
print ("\n\n\n")
print (number1, "+", number2, "=", add)
print (number1, "-", number2, "=", sub)
print (number1, "*", number2, "=", mult)
print (number1, "/", number2, "=", div)
print (number1, "%", number2, "=", mod)



                  


